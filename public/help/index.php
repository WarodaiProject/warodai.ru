    <h2>Как устроена словарная статья?</h2>

    <p>
        Результатом поиска по введенному вами ключевому слову является список
        словарных статей. Ниже представлено несколько&nbsp;схем типичных словарных статей:
    </p>

    <p>
        <strong>Схема 1</strong><br/>
        <img style="width:100%;max-width:650px;" src="img/help-1.png"/>
    </p>

    <p>
        <strong>1</strong> - транскрипция заглавного&nbsp;японского слова слоговой азбукой;<br/>
        <strong>2</strong> - варианты иероглифического написания японского слова, разделенные символом ･;<br/>
        <strong>3 </strong>- транскрипция японского слова по системе Поливанова<br/>
        <strong>4</strong> - идентификатор словарной карточки;<br/>
        <strong>5</strong> - русское значение<br/>
        <strong>6</strong> - устойчивые выражения или примеры с заглавным словом<br/>
        <strong>7</strong> - помета.
    </p>

    <p>
        <strong>Схема 2<br/>
        </strong><img style="width:100%;max-width:780px" src="img/help-2.png"/>
    </p>

    <p>
        <strong>1,2</strong> - транскрипция слоговой азбукой. В данной статье объединены сразу две
        японских лексемы: かじき и かじきまぐろ. Объединение в одной статье нескольких лексем (гнездование)
        производится в том случае, если их значения абсолютно синонимичны.&nbsp;Транcкрипции лексем
        разделяются запятой.<br/>
        <strong>3, 4</strong> - иероглифические написания лексем. Написания, относящиеся к разным
        лексемам, разделяются запятой. Однако в данном случае наличиствует по несколько вариантов
        написания каждой из лексем: для かじき это 旗魚 или 羽魚 или 舵木 или 梶木, для かじきまぐろ - 旗魚鮪 или 羽魚鮪 или
        舵木まぐろ или 梶木まぐろ. Варианты иероглифических написаний одной и той же лексемы разделяются символом
        ･.<br/>
        <strong>5 </strong>- желтым цветом в тексте статьи выделена подстрока, введенная вами при поиске
    </p>

    <p>
        <strong>Схема 3</strong><br/>
        <img style="width:100%;max-width:534px;" src="img/help-3.png"/>
    </p>

    <p>
        <strong>1 </strong>- номер значения. Если у слова более одного значения, они располагаются под
        номерам 1), 2), 3) и т.д.<br/>
        <strong>2</strong> - ссылка на другую статью<br/>
        <strong>3</strong> - фразеологизм. Фразеологизмы обозначаются в тексте статьи символом ◇.
    </p>
    
